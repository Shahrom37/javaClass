package ClassesAndObjectsPartOne;

public class Dog {
    public String name;
    public String breed;
    public String color;
    public int age;
    public int size;

    public void bark() {
        System.out.println(name + " is barking");
    }

    public void eat() {
        System.out.println(name + " is eating");

    }

    public void run() {
        System.out.println(name + " is running");
    }

    public void play() {
        System.out.println(name + " is playing");

    }
}